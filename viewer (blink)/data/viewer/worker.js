self.onmessage = e => {
  const request = e.data;

  if (request.cmd === 'execute') {
    if (FS.analyzePath('/work').object.mount.mountpoint === '/work') {
      FS.unmount('/work');
    }
    FS.mount(WORKERFS, {
      files: [request.file]
    }, '/work');

    FS.writeFile(request.program.name, request.program.code, {
      encoding: 'utf8'
    });

    Module.callMain(['usr/bin/perl', request.program.name, '/work/' + request.file.name]);
  }
};

